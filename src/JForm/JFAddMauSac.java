    private void btnThemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnThemMouseClicked
        String ThuocTinh = txtThuocTinh.getText();
        if (!ThuocTinh.isEmpty()) {
            if (ThuocTinh.length() > 50) {
                JOptionPane.showMessageDialog(this, "Kí tự vượt quá cho phép");
            } else {
                JOptionPane.showMessageDialog(this, MauSacService.add(ThuocTinh));
                FillToTableThuoTinh();
                FillToTableThungracThuoTinh();
                f3.fillToComboBox();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Không được để trống");
        }

    }//GEN-LAST:event_btnThemMouseClicked

    private void btnXoaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnXoaMouseClicked
        // Xóa thuộc tính và đưa vào thùng rác
        boolean itemDeleted = false;
        tbl = (DefaultTableModel) tblThuocTinh.getModel();
        for (int i = 0; i < tbl.getRowCount(); i++) {
            Boolean checked = (Boolean) tbl.getValueAt(i, 2);
            if (checked != null && checked) {
                itemDeleted = true;
                Integer id = (Integer) tbl.getValueAt(i, 0);
                MauSacService.DayVaoThungRac(id);
               
            }
        }
        System.out.println(itemDeleted);
        if (itemDeleted) {
            FillToTableThuoTinh();
            FillToTableThungracThuoTinh();
            f3.fillToComboBox();
            System.out.println("dsd");
        } else {
            System.out.println("Bạn chưa chọn đối tượng nào");
        }

    }//GEN-LAST:event_btnXoaMouseClicked

    private void btnSuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSuaMouseClicked
        rowselect = tblThuocTinh.getSelectedRow();
        tbl = (DefaultTableModel) tblThuocTinh.getModel();
        if (rowselect == -1) {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn đối tượng nào");
        } else {
            int id = (int) tbl.getValueAt(rowselect, 0);
            String ten = txtThuocTinh.getText();
            if (ten == null || ten.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Bạn chưa nhập dữ liệu muốn sửa vào");
            } else {
                MauSac tt = new MauSac(id, ten);
                MauSacService.update(tt);
                FillToTableThuoTinh();
                FillToTableThungracThuoTinh();
                f3.fillToComboBox();
                JOptionPane.showMessageDialog(this, "Đã sửa thành công");
            }
        }

    }//GEN-LAST:event_btnSuaMouseClicked

    private void btnKhoiPhucMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnKhoiPhucMouseClicked
        boolean itemSelected = false;
        tbl = (DefaultTableModel) tblThungRacThuocTinh.getModel();
        for (int i = 0; i < tbl.getRowCount(); i++) {
            Boolean checked = (Boolean) tbl.getValueAt(i, 2);
            if (checked != null && checked) {
                itemSelected = true;
                Integer id = (Integer) tbl.getValueAt(i, 0);
                MauSacService.returnItem(id);
            }
        }
        if (itemSelected) {
            FillToTableThuoTinh();
            FillToTableThungracThuoTinh();
            f3.fillToComboBox();
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn đối tượng nào");
        }
    }//GEN-LAST:event_btnKhoiPhucMouseClicked


