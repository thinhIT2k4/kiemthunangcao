    public void fillToTable() {
        ArrayList<SanPhamChiTiet> list = new ArrayList<>();
        tbl = (DefaultTableModel) tblThungRacSPCT.getModel();
        tbl.setRowCount(0);
        list = (ArrayList<SanPhamChiTiet>) SPCTService.selectAllThungRac();
        for (SanPhamChiTiet s : list) {
            tbl.addRow(new Object[]{i++, s.getID(), s.getTenSanPham(), s.getNXS(), s.getXuatXu(), s.getGiaBan(), s.getKichCo(), s.getMauSac(), s.getDangDan(), s.getSoLuong()});
        }
    }

