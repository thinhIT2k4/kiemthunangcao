    private void btnKhoiPhucThungRacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKhoiPhucThungRacActionPerformed

        if (i == -1) {
            // Không có dòng nào được chọn
            JOptionPane.showMessageDialog(this, "Bạn chưa chọn đối tượng khôi phục!");
        } else {
            int id = Integer.parseInt(tblThungRacSPCT.getValueAt(selectRow, 1).toString());
            System.out.println("id dòng được chọn là : " + id);
            if (id == -1) {
                JOptionPane.showMessageDialog(this, "Bạn chưa chọn đối tượng khôi phục!");
            } else {
                SPCTService.returnItem(id);
                f3.fillToTableSanPhamChiTiet();
                f3.fillToComboBox();
            }
        }
        fillToTable();
    }//GEN-LAST:event_btnKhoiPhucThungRacActionPerformed

